
public class Main {

	public static void main(String[] args) {

		// Final Keyword = anything that is declared as 'final' can not be changed or updated later in your program
		
		
		// Behaves as a const in JavaScript. ALL CAPITALIZED LETTERS IS GOOD PRACTICE
		final double PI = 3.14159;
		
		//pi = 4; -> ERROR	
		
		System.out.println(PI);

	}

}
